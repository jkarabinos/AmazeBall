    NSNumber* currentLevel = [defaults objectForKey:@"currentLevel"];
    if(!currentLevel){
        [defaults setObject:[NSNumber numberWithInt:0] forKey:@"currentLevel"];
    }
    
    NSNumber* playableLevel = [defaults objectForKey:@"playableLevel"];
    if(!playableLevel){
        [defaults setObject:[NSNumber numberWithInt:0] forKey:@"playableLevel"];
    }

    return YES;
}
							
